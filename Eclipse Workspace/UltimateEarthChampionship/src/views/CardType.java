package views;

/**
 * Enum to represent different types of cards.
 * STANDARD: a standard card without any special buying or selling properties.
 * CAN_BUY: a card that can be bought by the player.
 * CAN_SELL: a card that can be sold by the player.
 */
public enum CardType {
	STANDARD,
	CAN_BUY,
	CAN_SELL
}
