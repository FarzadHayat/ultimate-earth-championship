package model;

/**
 * Represents the champion stat that the player has selected to level up for a
 * champion.
 */
public enum LevelUpStat {
	/**
	 * Level up the stamina
	 */
	STAMINA,
	/**
	 * Level up the regen
	 */
	REGEN,
	/**
	 * Level up the offense
	 */
	OFFENSE,
	/**
	 * Level up the defense
	 */
	DEFENSE
}
